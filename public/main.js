const tokenAddress = '0x2Ad2934d5BFB7912304754479Dd1f096D5C807Da';
const burnContractAddress = '0x7e41b691f750238bF5a7A8b00f33EEbC3C23c739';

let tokenContract;
let burnContract;

window.addEventListener('load', async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        tokenContract = new web3.eth.Contract(tokenAbi, tokenAddress);
        burnContract = new web3.eth.Contract(burnContractAbi, burnContractAddress);
    } else {
        alert('MetaMask not detected. Please install MetaMask.');
    }
});

async function approve() {
    const amount = document.getElementById('amountApprove').value;
    try {
        const result = await tokenContract.methods.approve(burnContractAddress, amount).send({ from: userAccount });
        document.getElementById('resultApprove').innerText = `Approved: ${result.transactionHash}`;
    } catch (error) {
        console.error('Error:', error);
        document.getElementById('resultApprove').innerText = 'Error occurred. Check console for details.';
    }
}

